import setting

'''
########################################################################################################################
#                                                                                                                      #
########################################################################################################################
'''
class BinarySetting(setting.Setting):
    
    '''
    ####################################################################################################################
    #                                                                                                                  #
    ####################################################################################################################
    '''
    def __init__(self, config_data):
        super.__init__(config_data)

        self._on = config_data['on']
        self._off= config_data['off']

    '''
    ####################################################################################################################
    #                                                                                                                  #
    ####################################################################################################################
    '''
    def getValue(self):
        if self._rawValue == 1:
            return self._on
        else:
            return self._off

    '''
    ####################################################################################################################
    #                                                                                                                  #
    ####################################################################################################################
    '''
    def validateValue(self, value):
        return (value == 1) or (value == 0)
