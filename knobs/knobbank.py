import knobs.knob as knob

'''
########################################################################################################################
#                                                                                                                      #
########################################################################################################################
'''
class KnobBank():

    '''
    ####################################################################################################################
    #                                                                                                                  #
    ####################################################################################################################
    '''
    def __init__(self, config_file):
        
        self._knobs = {}

        for id, knobConfig in config_file.items():
            self._knobs[id] = knob.Knob(int(id), knobConfig['name'])

    '''
    ####################################################################################################################
    #                                                                                                                  #
    ####################################################################################################################
    '''
    def __str__(self):

        toReturn = ''

        for knob in self._knobs.values():
            toReturn += (str(knob) + '\n')

        return toReturn

